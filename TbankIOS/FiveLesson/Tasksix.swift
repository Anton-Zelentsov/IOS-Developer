import Foundation

// Создаю класс Animal
class Animal{
    func speak(){
        print("Animal sound")
    }
}

// Создаю подкласс Dog
class Dog: Animal{
    override func speak(){
        print("Woof!")
    }
}

// Создаю подкласс Cat
class Cat: Animal{
    override func speak(){
        print("Meow!")
    }
}
