 Задание:
 Реализуйте базовый класс Animal и два его подкласса: Dog и Cat.
 Напишите метод speak() в каждом из классов, который выводит соответствующий звук:
 Dog: "Woof!"
 Cat: "Meow!"
 Создайте массив animals, который будет содержать объекты разных животных (например, два объекта Dog и один объект Cat).
 Используйте цикл для перебора массива и вызова метода speak() для каждого животного.
